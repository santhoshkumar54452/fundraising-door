package model;

public class Fundraiser {
	private String RaiserID;
	private String RaiserName;
	private int age;
	private String Problem;
	private int amount;
	private int Count;
	
	public String getRaiserID() {
		return RaiserID;
	}
	public void setRaiserID(String RaiserID) {
		this.RaiserID = RaiserID;
	}
	public String getRaiserName() {
		return RaiserName;
	}
	public void setRaiserName(String RaiserName) {
		this.RaiserName = RaiserName;
	}
	public int getage() {
		return age;
	}
	public void setage(int age) {
		this.age = age;
	}
	public String getProblem() {
		return Problem;
	}
	public void setProblem(String Problem) {
		this.Problem = Problem;
	}
	public int getamount() {
		return amount;
	}
	public void setamount(int amount) {
		this.amount = amount;
	}
	public int getCount() {
		return Count;
	}
	public void setCount(int Count) {
		this.Count = Count;
	}
	
}


